package salida

import (
	"fmt"
	"os"
)

// -----------------------------------------------------------------------------------------------------------------------------------
//                                                  	Informar error
// -----------------------------------------------------------------------------------------------------------------------------------

func ImprimirError(err error) {
	fmt.Fprintf(os.Stdout, "%s\n", err.Error())
}

// -----------------------------------------------------------------------------------------------------------------------------------
//                                                  Informar comando bien ejecutado
// -----------------------------------------------------------------------------------------------------------------------------------

const (
	MENSAJE_COMANDO_EJECUTADO_OK = "OK"
)

func ImprimirComandoOK() {
	fmt.Fprintf(os.Stdout, "%s\n", MENSAJE_COMANDO_EJECUTADO_OK)
}
